      SUBROUTINE INVERS(IPRINV)
      COMMON /INVER1/ UI1,VI1,WI1,UI2,VI2,WI2,UI3,VI3,WI3
      COMMON /MATR1/ U1,V1,W1,U2,V2,W2,U3,V3,W3
C
C
C     SUBROUTINE 'INVERS' ...
C        CALCULATE THE INVERSE OF A 3 BY 3 MATRIX
C
C
C
C     --- CALCULATE DETERMINANT
      DET = U1*V2*W3 + V1*W2*U3 + W1*U2*V3 - U3*V2*W1 - V3*W2*U1 -
     $      W3*U2*V1
C
C     --- CALCULATE INVERSE
      UI1 =  (V2*W3 - V3*W2)/DET
      VI1 = -(V1*W3 - V3*W1)/DET
      WI1 =  (V1*W2 - V2*W1)/DET
      UI2 = -(U2*W3 - U3*W2)/DET
      VI2 =  (U1*W3 - U3*W1)/DET
      WI2 = -(U1*W2 - U2*W1)/DET
      UI3 =  (U2*V3 - U3*V2)/DET
      VI3 = -(U1*V3 - U3*V1)/DET
      WI3 =  (U1*V2 - U2*V1)/DET
C
C     --- PRINT OUT FOR INVERSE MATRIX
      IF(IPRINV.EQ.1) CALL OUTPT1(12)
      RETURN
      END
